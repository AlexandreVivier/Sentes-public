                <h3 class="index-title special-elite-regular">Qu'est-ce que les Sentes ?</h3>
                <p class="text-green text-medium italic text-interline">
                    Les Sentes est un GN en kit à monter soi-même. Initialement, ceci est le jeu des drames forestiers dans une réalité sorcière. Il est composé de briques de base, des briques d'intrigues (situations, temps forts, communautés) et des briques de personnages (missions de vie, destins, rituels, souvenirs, etc) que les orgas et les joueuses peuvent assembler pour personnaliser leur expérience.
                </p>
                <p class="text-green text-medium italic text-interline">
                    Si Les Sentes est au départ un GN censé se passer dans l'univers post-apocalyptique forestier de Millevaux, il est en réalité possible de décaler dans d'autres univers, moyennant une réécriture plus ou moins approfondie des briques de base.
                </p>
                <p class="text-green text-medium italic text-interline">
                    En tant que créatrice d'une édition du GN Les Sentes, il vous faudra trouver un thème (Les Sentes neutre ou adaptation), vous coordonner avec votre équipe voir avec l'ensemble des joueuses sur les aspects logistiques (date, site de jeu, cuisine, logement, covoiturage...)
                </p>
                <p class="text-green text-medium italic text-interline">
                    Puis vous devrez créer l'intrigue en assemblant les briques d'intrigue de votre choix et ensuite permettre aux joueuses de créer leurs personnages et de les rendre publics sur le site afin d'établir les liens relationnels entre personnages.
                </p>
